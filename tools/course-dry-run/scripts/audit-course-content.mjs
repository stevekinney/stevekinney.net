function parseArguments(argv) {
	const argumentsMap = {};

	for (let index = 0; index < argv.length; index += 1) {
		const token = argv[index];

		if (token === '--help') {
			argumentsMap.help = true;
			continue;
		}

		if (!token.startsWith('--')) {
			throw new Error(`Unexpected argument: ${token}`);
		}

		const key = token.slice(2);
		const value = argv[index + 1];

		if (!value || value.startsWith('--')) {
			throw new Error(`Missing value for --${key}`);
		}

		argumentsMap[key] = value;
		index += 1;
	}

	return argumentsMap;
}

function normalizeWhitespace(value) {
	return value
		.replace(/\r\n/g, '\n')
		.replace(/[ \t]+/g, ' ')
		.trim();
}

function normalizeLeadingWhitespace(value) {
	return value.replace(/^[ \t]+/gm, (indentation) => indentation.replace(/\t/g, '  '));
}

function stripIndent(value) {
	const lines = normalizeLeadingWhitespace(value).replace(/\r\n/g, '\n').split('\n');

	while (lines.length > 0 && lines[0].trim() === '') {
		lines.shift();
	}

	while (lines.length > 0 && lines.at(-1)?.trim() === '') {
		lines.pop();
	}

	const indentationLevels = lines
		.filter((line) => line.trim() !== '')
		.map((line) => line.match(/^ */)?.[0].length ?? 0);
	const minimumIndentation = indentationLevels.length > 0 ? Math.min(...indentationLevels) : 0;

	return lines.map((line) => line.slice(minimumIndentation)).join('\n');
}

function normalizeSnippet(value) {
	return stripIndent(value).replace(/\r\n/g, '\n').trim();
}

function unique(values) {
	return [...new Set(values)];
}

function isLikelyPathToken(token) {
	if (!token || /\s{2,}/.test(token) || /^https?:\/\//.test(token)) {
		return false;
	}

	if (
		/^(?:\.github|src|tests|scripts|docs|assets|tools|playwright-report|tmp)\//.test(token) ||
		/^\.?[A-Za-z0-9_./-]+\.[A-Za-z0-9._-]+$/.test(token)
	) {
		return true;
	}

	return ['package.json', 'tsconfig.json', 'lefthook.yml', '.gitleaks.toml', '.mcp.json'].includes(
		token
	);
}

function extractPathTokens(text) {
	const pathTokens = [];

	for (const match of text.matchAll(/`([^`\n]+)`/g)) {
		const candidate = match[1].trim();

		if (isLikelyPathToken(candidate)) {
			pathTokens.push(candidate);
		}
	}

	return unique(pathTokens);
}

function extractCommandTokens(text, codeBlocks = []) {
	const commands = [];

	for (const match of text.matchAll(/`((?:npm|npx|git|node|bun)\s+[^`\n]+)`/g)) {
		commands.push(normalizeWhitespace(match[1]));
	}

	for (const codeBlock of codeBlocks) {
		if (!['sh', 'bash'].includes(codeBlock.language)) {
			continue;
		}

		for (const line of codeBlock.content.split('\n')) {
			const trimmedLine = line.trim();

			if (!trimmedLine || trimmedLine.startsWith('#')) {
				continue;
			}

			if (/^(npm|npx|git|node|bun)\s+/.test(trimmedLine)) {
				commands.push(normalizeWhitespace(trimmedLine));
			}
		}
	}

	return unique(commands);
}

function isRepeatedSetupCommand(command) {
	return /^(npm run dev|npm run preview|git clone|npx playwright install\b)/.test(command);
}

function resolvePathToken(token, repositoryIndex, courseIndex) {
	if (path.isAbsolute(token)) {
		if (repositoryIndex.absolutePaths.has(token) || courseIndex.absolutePaths.has(token)) {
			return token;
		}

		return null;
	}

	const repositoryCandidate = path.resolve(repositoryIndex.root, token);
	const courseCandidate = path.resolve(courseIndex.root, token);
	const normalizedCourseCandidate = path.resolve(courseIndex.root, token.replace(/^\.?\//, ''));

	for (const candidate of [repositoryCandidate, courseCandidate, normalizedCourseCandidate]) {
		if (repositoryIndex.absolutePaths.has(candidate) || courseIndex.absolutePaths.has(candidate)) {
			return candidate;
		}

		if (existsSync(candidate)) {
			return candidate;
		}
	}

	if (!token.includes('/')) {
		const repositoryMatches = repositoryIndex.byBasename.get(token) ?? [];
		const courseMatches = courseIndex.byBasename.get(token) ?? [];
		const matches = [...repositoryMatches, ...courseMatches];

		if (matches.length === 1) {
			return matches[0];
		}
	}

	return null;
}

function buildTrackedFileIndex(rootPath) {
	let trackedFiles;

	try {
		const rawOutput = execFileSync('git', ['-C', rootPath, 'ls-files'], {
			encoding: 'utf8',
			stdio: ['ignore', 'pipe', 'ignore']
		});
		trackedFiles = rawOutput.split('\n').filter(Boolean);
	} catch {
		trackedFiles = [];
	}

	const byBasename = new Map();
	const absolutePaths = new Set();

	for (const trackedFile of trackedFiles) {
		const absolutePath = path.resolve(rootPath, trackedFile);
		const baseName = path.basename(trackedFile);

		absolutePaths.add(absolutePath);

		if (!byBasename.has(baseName)) {
			byBasename.set(baseName, []);
		}

		byBasename.get(baseName).push(absolutePath);
	}

	return {
		root: rootPath,
		trackedFiles,
		absolutePaths,
		byBasename
	};
}

async function readJsonIfPresent(filePath) {
	try {
		return JSON.parse(await readFile(filePath, 'utf8'));
	} catch (error) {
		if (error instanceof Error && 'code' in error && error.code === 'ENOENT') {
			return null;
		}

		throw error;
	}
}

function getContextText(entrySource, codeBlock, radius = 8) {
	const lines = entrySource.split('\n');
	const startIndex = Math.max(0, codeBlock.lineStart - 1 - radius);
	const endIndex = Math.min(lines.length, codeBlock.lineEnd + radius);
	return lines.slice(startIndex, endIndex).join('\n');
}

function getLeadingContextText(entrySource, codeBlock, radius = 4) {
	const lines = entrySource.split('\n');
	const startIndex = Math.max(0, codeBlock.lineStart - 1 - radius);
	const endIndex = Math.max(0, codeBlock.lineStart - 1);
	return lines.slice(startIndex, endIndex).join('\n');
}

function chooseValidationStrategy(language) {
	switch (language) {
		case 'ts':
		case 'js':
			return 'typescript';
		case 'svelte':
			return 'svelte';
		case 'sh':
		case 'bash':
			return 'shell';
		case 'json':
		case 'jsonc':
			return 'json-like';
		case 'yaml':
		case 'yml':
			return 'yaml';
		case 'toml':
			return 'toml';
		case 'markdown':
		case 'md':
			return 'markdown';
		case 'mermaid':
			return 'illustration';
		case 'diff':
		case 'text':
		case '(none)':
			return 'illustration';
		default:
			return 'illustration';
	}
}

function isCompatibleReferencePath(referencePath, strategy) {
	const extension = path.extname(referencePath).toLowerCase();
	const baseName = path.basename(referencePath).toLowerCase();

	switch (strategy) {
		case 'typescript':
			return ['.ts', '.tsx', '.js', '.jsx', '.mjs', '.cjs'].includes(extension);
		case 'svelte':
			return extension === '.svelte';
		case 'shell':
			return extension === '.sh';
		case 'json-like':
			return ['.json', '.jsonc'].includes(extension) || baseName === 'package.json';
		case 'yaml':
			return ['.yml', '.yaml'].includes(extension);
		case 'toml':
			return extension === '.toml';
		case 'markdown':
			return extension === '.md';
		case 'illustration':
			return false;
		default:
			return false;
	}
}

function looksIllustrativePartial(codeBlock, contextText) {
	const lowerCaseContext = contextText.toLowerCase();
	const uncommentedContent = codeBlock.content
		.split('\n')
		.filter((line) => !line.trim().startsWith('//'))
		.join('\n');

	if (/\bexcerpt\b|\bwalk\b|\bfind\b|\bnotice\b|\blines?\b/.test(lowerCaseContext)) {
		return true;
	}

	if (/[<[][^>\]]+[>\]]/.test(codeBlock.content) || /\.\.\./.test(codeBlock.content)) {
		return true;
	}

	if (['ts', 'js', 'svelte'].includes(codeBlock.language)) {
		if (
			/^\s*[A-Za-z_$][\w$]*\s*:\s*/m.test(uncommentedContent) &&
			!/^\s*(import|export|const|let|function|class|type|interface|return)\b/m.test(
				uncommentedContent
			)
		) {
			return true;
		}

		return !/\b(import|export|const|let|function|class|type|interface|test|async)\b/.test(
			uncommentedContent
		);
	}

	return false;
}

async function validateWithTypeScript(code, extension) {
	const result = typeScript.transpileModule(code, {
		compilerOptions: {
			module: typeScript.ModuleKind.ESNext,
			target: typeScript.ScriptTarget.ES2022,
			strict: true
		},
		fileName: `snippet.${extension}`,
		reportDiagnostics: true
	});
	const diagnostics = (result.diagnostics ?? []).filter(
		(diagnostic) => diagnostic.category === typeScript.DiagnosticCategory.Error
	);

	if (diagnostics.length > 0) {
		throw new Error(
			diagnostics
				.map((diagnostic) => typeScript.flattenDiagnosticMessageText(diagnostic.messageText, '\n'))
				.join('\n')
		);
	}
}

async function validateWithSvelte(code) {
	svelteCompiler.compile(code, {
		generate: 'server'
	});
}

async function validateWithShell(code, shell) {
	execFileSync(shell, ['-n'], {
		input: `${code}\n`,
		stdio: ['pipe', 'pipe', 'pipe']
	});
}

async function validateWithJsonLike(code, parserName) {
	if (parserName === 'json') {
		JSON.parse(code);
		return;
	}

	await prettier.format(code, { parser: 'json5' });
}

async function validateWithYaml(code) {
	await prettier.format(code, { parser: 'yaml' });
}

async function validateWithMarkdown(code) {
	await prettier.format(code, { parser: 'markdown' });
}

async function validateWithToml(code) {
	if (typeof parseToml !== 'function') {
		throw new Error('smol-toml parse() is unavailable.');
	}

	parseToml(code);
}

async function runStrategyValidation(strategy, codeBlock) {
	switch (strategy) {
		case 'typescript':
			await validateWithTypeScript(codeBlock.content, codeBlock.language === 'ts' ? 'ts' : 'js');
			return;
		case 'svelte':
			await validateWithSvelte(codeBlock.content);
			return;
		case 'shell':
			await validateWithShell(codeBlock.content, codeBlock.language === 'bash' ? 'bash' : 'sh');
			return;
		case 'json-like':
			await validateWithJsonLike(codeBlock.content, codeBlock.language);
			return;
		case 'yaml':
			await validateWithYaml(codeBlock.content);
			return;
		case 'markdown':
			await validateWithMarkdown(codeBlock.content);
			return;
		case 'toml':
			await validateWithToml(codeBlock.content);
			return;
		case 'illustration':
			return;
		default:
			return;
	}
}

function isMatchingReference(snippetContent, referenceContent) {
	const normalizedSnippet = normalizeSnippet(snippetContent);
	const normalizedReference = normalizeSnippet(referenceContent);

	if (normalizedSnippet.length === 0) {
		return false;
	}

	if (normalizedReference.includes(normalizedSnippet)) {
		return true;
	}

	const snippetLines = normalizedSnippet.split('\n');
	const referenceLines = referenceContent.replace(/\r\n/g, '\n').split('\n');

	for (let index = 0; index <= referenceLines.length - snippetLines.length; index += 1) {
		const candidateWindow = referenceLines.slice(index, index + snippetLines.length).join('\n');

		if (normalizeSnippet(candidateWindow) === normalizedSnippet) {
			return true;
		}
	}

	return false;
}

async function getReferencePaths(entrySource, codeBlock, repositoryIndex, courseIndex) {
	const localContext = getContextText(entrySource, codeBlock);
	const localTokens = extractPathTokens(localContext);
	const resolvedLocalTokens = [];

	for (const token of localTokens) {
		const resolvedPath = resolvePathToken(token, repositoryIndex, courseIndex);

		if (resolvedPath) {
			resolvedLocalTokens.push(resolvedPath);
		}
	}

	if (localTokens.length > 0) {
		return unique(resolvedLocalTokens);
	}

	const entryTokens = extractPathTokens(entrySource);
	const resolvedEntryTokens = [];

	for (const token of entryTokens) {
		const resolvedPath = resolvePathToken(token, repositoryIndex, courseIndex);

		if (resolvedPath) {
			resolvedEntryTokens.push(resolvedPath);
		}
	}

	return unique(resolvedEntryTokens);
}

async function buildSnippetCheck(
	codeBlock,
	entrySource,
	fileContentCache,
	repositoryIndex,
	courseIndex
) {
	const strategy = chooseValidationStrategy(codeBlock.language);
	const contextText = getContextText(entrySource, codeBlock);
	const leadingContextText = getLeadingContextText(entrySource, codeBlock);
	const referencePaths = (
		await getReferencePaths(entrySource, codeBlock, repositoryIndex, courseIndex)
	).filter((referencePath) => isCompatibleReferencePath(referencePath, strategy));
	const exactnessRequired =
		exactnessCuePattern.test(leadingContextText) &&
		!createOrCopyCuePattern.test(leadingContextText);

	for (const referencePath of referencePaths) {
		let referenceContent = fileContentCache.get(referencePath);

		if (!referenceContent) {
			referenceContent = await readFile(referencePath, 'utf8');
			fileContentCache.set(referencePath, referenceContent);
		}

		if (isMatchingReference(codeBlock.content, referenceContent)) {
			return {
				index: codeBlock.index,
				language: codeBlock.language,
				lineStart: codeBlock.lineStart,
				lineEnd: codeBlock.lineEnd,
				heading: codeBlock.heading,
				strategy,
				status: 'matches-reference',
				referencePath,
				details: 'Snippet matches a referenced shipped file.'
			};
		}
	}

	if (exactnessRequired && referencePaths.length > 0) {
		return {
			index: codeBlock.index,
			language: codeBlock.language,
			lineStart: codeBlock.lineStart,
			lineEnd: codeBlock.lineEnd,
			heading: codeBlock.heading,
			strategy,
			status: 'failed',
			referencePath: referencePaths[0],
			details:
				'Snippet claims to show the shipped shape, but it does not match the referenced file.'
		};
	}

	if (strategy === 'illustration') {
		return {
			index: codeBlock.index,
			language: codeBlock.language,
			lineStart: codeBlock.lineStart,
			lineEnd: codeBlock.lineEnd,
			heading: codeBlock.heading,
			strategy,
			status: 'documented-illustration',
			details: 'Illustrative block recorded without execution.'
		};
	}

	try {
		await runStrategyValidation(strategy, codeBlock);
		return {
			index: codeBlock.index,
			language: codeBlock.language,
			lineStart: codeBlock.lineStart,
			lineEnd: codeBlock.lineEnd,
			heading: codeBlock.heading,
			strategy,
			status: 'syntax-valid',
			details: 'Snippet passed lightweight validation.'
		};
	} catch (error) {
		if (!exactnessRequired && looksIllustrativePartial(codeBlock, contextText)) {
			return {
				index: codeBlock.index,
				language: codeBlock.language,
				lineStart: codeBlock.lineStart,
				lineEnd: codeBlock.lineEnd,
				heading: codeBlock.heading,
				strategy,
				status: 'illustrative-partial',
				details:
					'Snippet behaves like an explanatory excerpt rather than a standalone runnable block.'
			};
		}

		return {
			index: codeBlock.index,
			language: codeBlock.language,
			lineStart: codeBlock.lineStart,
			lineEnd: codeBlock.lineEnd,
			heading: codeBlock.heading,
			strategy,
			status: 'failed',
			details: error instanceof Error ? error.message : String(error)
		};
	}
}

function buildCoverageFindings(entry, entrySource, entryTextByHref, entryCodeBlocksByHref) {
	if (entry.kind !== 'lab') {
		return [];
	}

	const relatedLessonText = entry.related
		.filter((relatedEntry) => relatedEntry.kind === 'lesson')
		.map((relatedEntry) => entryTextByHref.get(relatedEntry.href) ?? '')
		.join('\n');
	const findings = [];
	const namedCommands = extractCommandTokens(entrySource, entry.codeBlocks).filter((command) =>
		command.startsWith('npm run ')
	);
	const referencedPaths = extractPathTokens(entrySource).filter((token) => !token.endsWith('.md'));

	for (const referencedPath of referencedPaths.slice(0, 6)) {
		if (!relatedLessonText.includes(referencedPath)) {
			findings.push({
				severity: 'moderate',
				message: `Lab references \`${referencedPath}\`, but the related lesson never names it.`,
				evidence: referencedPath
			});
		}
	}

	for (const command of namedCommands.slice(0, 6)) {
		if (!relatedLessonText.includes(command)) {
			findings.push({
				severity: 'minor',
				message: `Lab asks the reader to run \`${command}\`, but the related lesson never introduces that command.`,
				evidence: command
			});
		}
	}

	const relatedSolutions = entry.related
		.filter((relatedEntry) => relatedEntry.kind === 'solution')
		.map((relatedEntry) => entryCodeBlocksByHref.get(relatedEntry.href)?.length ?? 0)
		.reduce((total, value) => total + value, 0);

	if (
		relatedSolutions === 0 &&
		entry.related.some((relatedEntry) => relatedEntry.kind === 'solution')
	) {
		findings.push({
			severity: 'minor',
			message: 'Related solution page exists but contains no fenced code blocks to validate.',
			evidence: 'solution-page-without-snippets'
		});
	}

	return findings;
}

function buildVisibilityFindings(entry, entrySource) {
	if (!visualCuePattern.test(entrySource) || /!\[[^\]]*]\([^)]+\)/.test(entrySource)) {
		return [];
	}

	return [
		{
			severity: 'moderate',
			message:
				'The prose promises a visual success state, but the page has no embedded image to show it.',
			evidence: 'visual-cue-without-image'
		}
	];
}

function buildFrictionFindings(entry, entrySource, setupCommandFrequency) {
	const findings = [];

	if (
		entry.kind === 'lab' &&
		shippedCuePattern.test(entrySource) &&
		createOrCopyCuePattern.test(entrySource)
	) {
		findings.push({
			severity: 'moderate',
			disposition: /\bwalkthrough\b/i.test(entrySource)
				? 'rewrite-as-walkthrough'
				: 'pre-ship-scaffold',
			message:
				'This lab asks the reader to recreate or copy work even though the starter already ships the final shape.',
			evidence: 'starter-already-ships-target'
		});
	}

	for (const command of extractCommandTokens(entrySource, entry.codeBlocks)) {
		if (!isRepeatedSetupCommand(command)) {
			continue;
		}

		if ((setupCommandFrequency.get(command) ?? 0) < 2) {
			continue;
		}

		findings.push({
			severity: 'minor',
			disposition: 'cross-reference-existing-section',
			message: `Repeated setup command \`${command}\` appears across multiple labs and should be centralized.`,
			evidence: command
		});
	}

	return findings;
}

function buildFixes(entry, coverageFindings, visibilityFindings, frictionFindings) {
	const fixes = [];

	for (const finding of coverageFindings) {
		fixes.push({
			target: 'course',
			reason: finding.message,
			requiredBefore: entry.href,
			publishImpact: 'course-branch'
		});
	}

	for (const finding of visibilityFindings) {
		fixes.push({
			target: 'course',
			reason: finding.message,
			requiredBefore: entry.href,
			publishImpact: 'course-branch'
		});
	}

	for (const finding of frictionFindings) {
		fixes.push({
			target: finding.disposition === 'pre-ship-scaffold' ? 'starter' : 'course',
			reason: finding.message,
			requiredBefore: entry.href,
			publishImpact:
				finding.disposition === 'pre-ship-scaffold' ? 'publish-mode-candidate' : 'course-branch'
		});
	}

	return fixes.filter(
		(fix, index) =>
			fixes.findIndex(
				(candidate) =>
					candidate.target === fix.target &&
					candidate.reason === fix.reason &&
					candidate.requiredBefore === fix.requiredBefore &&
					candidate.publishImpact === fix.publishImpact
			) === index
	);
}

function summarizeOutcome(snippetChecks, coverageFindings, visibilityFindings, frictionFindings) {
	const failedSnippets = snippetChecks.filter(
		(snippetCheck) => snippetCheck.status === 'failed'
	).length;
	const blockingFindings = [...coverageFindings, ...visibilityFindings, ...frictionFindings].filter(
		(finding) => finding.severity !== 'minor'
	).length;

	if (failedSnippets > 0 || blockingFindings > 0) {
		return 'Needs attention before publish mode.';
	}

	return 'Ready for branch-level fixes and follow-up execution.';
}

function buildSummary(manifest, results, outputDirectory) {
	const snippetChecks = results.flatMap((result) => result.snippetChecks);
	const failedSnippets = snippetChecks.filter(
		(snippetCheck) => snippetCheck.status === 'failed'
	).length;
	const courseFixes = results
		.flatMap((result) => result.fixes)
		.filter((fix) => fix.target === 'course').length;
	const starterFixes = results
		.flatMap((result) => result.fixes)
		.filter((fix) => fix.target === 'starter').length;
	const labsAwaitingExecution = results.filter(
		(result) => result.kind === 'lab' && result.labExecution?.status === 'pending-skill-run'
	).length;
	const labStatusCounts = new Map();
	const frictionDispositionCounts = new Map();

	for (const result of results) {
		if (result.kind === 'lab') {
			const status = result.labExecution?.status ?? 'missing';
			labStatusCounts.set(status, (labStatusCounts.get(status) ?? 0) + 1);
		}

		for (const finding of result.frictionFindings) {
			const key = finding.disposition ?? 'unspecified';
			frictionDispositionCounts.set(key, (frictionDispositionCounts.get(key) ?? 0) + 1);
		}
	}

	const frictionLines =
		frictionDispositionCounts.size === 0
			? '- none'
			: [...frictionDispositionCounts.entries()]
					.map(([disposition, count]) => `- ${disposition}: ${count}`)
					.join('\n');
	const labStatusLines =
		labStatusCounts.size === 0
			? '- none'
			: [...labStatusCounts.entries()]
					.sort(([leftStatus], [rightStatus]) => leftStatus.localeCompare(rightStatus))
					.map(([status, count]) => `- ${status}: ${count}`)
					.join('\n');

	return `# Course Dry-Run Summary

- Output directory: \`${outputDirectory}\`
- Entries: ${manifest.counts.total} total (${manifest.counts.lesson} lessons, ${manifest.counts.lab} labs, ${manifest.counts.solution} solutions)
- Snippet checks: ${snippetChecks.length} total, ${failedSnippets} failed
- Labs awaiting skill execution: ${labsAwaitingExecution}
- Course-targeted fixes: ${courseFixes}
- Starter-targeted fixes: ${starterFixes}

## Reader friction dispositions

${frictionLines}

## Lab execution

${labStatusLines}

## Next commands

- Refresh the Codex mirror: \`node tools/course-dry-run/install-codex-skill.mjs\`
- Publish mode stays opt-in: only reconcile \`main\` when the prompt explicitly says \`publish\` or \`reconcile main\`
`;
}

async function main() {
	const argumentsMap = parseArguments(process.argv.slice(2));

	if (argumentsMap.help) {
		printUsage();
		return;
	}

	if (!argumentsMap.manifest || !argumentsMap['out-dir']) {
		throw new Error('Both --manifest and --out-dir are required.');
	}

	const manifestPath = path.resolve(argumentsMap.manifest);
	const outputDirectory = path.resolve(argumentsMap['out-dir']);
	const repositoryRootPath = path.resolve(argumentsMap['repo-root'] ?? defaultStarterRoot);
	const manifest = JSON.parse(await readFile(manifestPath, 'utf8'));
	const courseRootPath = path.resolve(
		argumentsMap['course-root'] ?? manifest.courseRoot ?? defaultCourseRoot
	);
	const repositoryIndex = buildTrackedFileIndex(repositoryRootPath);
	const courseIndex = buildTrackedFileIndex(courseRootPath);
	const fileContentCache = new Map();
	const previousResultsPath = path.join(outputDirectory, 'validation-results.json');
	const previousResults = (await readJsonIfPresent(previousResultsPath))?.results ?? [];
	const previousLabExecutionByHref = new Map(
		previousResults
			.filter((result) => result.kind === 'lab' && result.labExecution)
			.map((result) => [result.href, result.labExecution])
	);
	const entryTextByHref = new Map();
	const entryCodeBlocksByHref = new Map();
	const setupCommandFrequency = new Map();

	for (const entry of manifest.entries) {
		const entrySource = await readFile(path.join(courseRootPath, entry.href), 'utf8');
		entryTextByHref.set(entry.href, entrySource);
		entryCodeBlocksByHref.set(entry.href, entry.codeBlocks ?? []);

		if (entry.kind !== 'lab') {
			continue;
		}

		for (const command of extractCommandTokens(entrySource, entry.codeBlocks ?? [])) {
			if (!isRepeatedSetupCommand(command)) {
				continue;
			}

			setupCommandFrequency.set(command, (setupCommandFrequency.get(command) ?? 0) + 1);
		}
	}

	const results = [];

	for (const entry of manifest.entries) {
		const entrySource = entryTextByHref.get(entry.href) ?? '';
		const snippetChecks = [];

		for (const codeBlock of entry.codeBlocks ?? []) {
			snippetChecks.push(
				await buildSnippetCheck(
					codeBlock,
					entrySource,
					fileContentCache,
					repositoryIndex,
					courseIndex
				)
			);
		}

		const coverageFindings = buildCoverageFindings(
			entry,
			entrySource,
			entryTextByHref,
			entryCodeBlocksByHref
		);
		const visibilityFindings = buildVisibilityFindings(entry, entrySource);
		const frictionFindings = buildFrictionFindings(entry, entrySource, setupCommandFrequency);
		const fixes = buildFixes(entry, coverageFindings, visibilityFindings, frictionFindings);
		const blockingItems = [...coverageFindings, ...visibilityFindings, ...frictionFindings].filter(
			(finding) => finding.severity !== 'minor'
		);
		const failedSnippets = snippetChecks.filter((snippetCheck) => snippetCheck.status === 'failed');

		results.push({
			href: entry.href,
			title: entry.title,
			kind: entry.kind,
			status: failedSnippets.length > 0 || blockingItems.length > 0 ? 'needs-attention' : 'ok',
			labExecution:
				entry.kind === 'lab'
					? (previousLabExecutionByHref.get(entry.href) ?? {
							status: 'pending-skill-run',
							reason:
								'The audit script validates course content. Populate labExecution during the actual lab-execution phase.'
						})
					: null,
			snippetChecks,
			coverageFindings,
			visibilityFindings,
			frictionFindings,
			fixes,
			finalOutcome: summarizeOutcome(
				snippetChecks,
				coverageFindings,
				visibilityFindings,
				frictionFindings
			)
		});
	}

	const validationResults = {
		generatedAt: new Date().toISOString(),
		repositoryRoot: repositoryRootPath,
		courseRoot: courseRootPath,
		manifestPath,
		results
	};
	const summary = buildSummary(manifest, results, outputDirectory);

	await mkdir(outputDirectory, { recursive: true });
	await writeFile(
		path.join(outputDirectory, 'validation-results.json'),
		`${JSON.stringify(validationResults, null, 2)}\n`
	);
	await writeFile(path.join(outputDirectory, 'summary.md'), summary, 'utf8');
	console.error(
		`Wrote validation results to ${path.join(outputDirectory, 'validation-results.json')}`
	);
	console.error(`Wrote summary to ${path.join(outputDirectory, 'summary.md')}`);
}

main().catch((error) => {
	console.error(error instanceof Error ? error.message : String(error));
	process.exitCode = 1;
});
